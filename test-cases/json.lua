local table_ser = require("table_serialization")
local json = require("json")
print(table_ser.dump(json))
print("=============encode==============")
print(json.encode(1))
print(json.encode("hhh"))
print(json.encode(true))
print(json.encode(nil))
print(json.encode({aa = 6, bb = "ccc"}))
print(json.encode({1,"222", "cc"}))
-- print(json.encode(print))
print("=============decode==============")
print(json.decode(json.encode(1)))
print(json.decode(json.encode("hhh")))
print(json.decode(json.encode(true)))
print(json.decode(json.encode(nil)))
print(table_ser.dump((json.decode(json.encode({aa = 6, bb = "ccc"})))))
print(table_ser.dump((json.decode(json.encode({1,"222", "cc"})))))

